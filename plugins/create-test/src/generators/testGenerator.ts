/**
 * Test Generator
 *
 * Produces complete, idiomatic test files from the structural metadata returned
 * by the file analyzer, for every framework the detector supports.
 *
 * Design principles:
 *   • Every generated test has a real assertion — no empty stubs.
 *   • External dependencies (non-relative imports) are mocked automatically.
 *   • Each exported function / class gets its own describe block.
 *   • Happy-path, edge-case, and error tests are generated for every symbol.
 *   • TODO comments guide developers toward test-specific context they must add.
 */

import * as path from 'path';
import type {
  AnalyzedFile,
  ClassInfo,
  FunctionInfo,
  ImportInfo,
  MethodInfo,
  ParameterInfo,
} from '../analyzers/fileAnalyzer';
import type { SupportedFramework } from '../utils/frameworkDetector';

// ─────────────────────────────────────────────────────────────────────────────
// Public API
// ─────────────────────────────────────────────────────────────────────────────

export interface GenerateOptions {
  /** When true, emit extra boundary / edge-case tests. */
  coverageHints?: boolean;
}

/**
 * Generate the full text of a test file for `analyzed` using `framework`.
 */
export function generateTests(
  analyzed: AnalyzedFile,
  framework: SupportedFramework,
  options: GenerateOptions = {},
): string {
  switch (framework) {
    case 'jest':
      return generateJestVitest(analyzed, 'jest', options);
    case 'vitest':
      return generateJestVitest(analyzed, 'vitest', options);
    case 'mocha':
      return generateMocha(analyzed, options);
    case 'pytest':
      return generatePytest(analyzed, options);
    case 'go-test':
      return generateGoTest(analyzed, options);
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// Jest / Vitest generator  (they share the same describe/it/expect API)
// ─────────────────────────────────────────────────────────────────────────────

function generateJestVitest(
  analyzed: AnalyzedFile,
  flavor: 'jest' | 'vitest',
  options: GenerateOptions,
): string {
  const lines: string[] = [];
  const sourceBase = moduleImportPath(analyzed.filePath);

  // ── file header ───────────────────────────────────────────────────────────
  lines.push(`// Generated by create-test — ${flavor}`);
  lines.push('');

  // ── vitest-specific: import test utilities ────────────────────────────────
  if (flavor === 'vitest') {
    lines.push(`import { describe, it, expect, vi, beforeEach } from 'vitest';`);
  }

  // ── mock external imports ─────────────────────────────────────────────────
  const externalImports = analyzed.imports.filter((i) => i.isExternal);
  const internalImports = analyzed.imports.filter((i) => !i.isExternal);

  for (const imp of externalImports) {
    const mock = flavor === 'vitest' ? 'vi.mock' : 'jest.mock';
    lines.push(`${mock}('${imp.modulePath}');`);
  }
  if (externalImports.length) lines.push('');

  // ── import the module under test ──────────────────────────────────────────
  const importedNames = buildImportList(analyzed);
  if (importedNames.length) {
    lines.push(`import { ${importedNames.join(', ')} } from '${sourceBase}';`);
  } else if (analyzed.hasDefaultExport) {
    const defaultName = path.basename(analyzed.filePath).replace(/\.[^.]+$/, '');
    lines.push(`import ${defaultName} from '${sourceBase}';`);
  }

  // ── mock internal imports ─────────────────────────────────────────────────
  if (internalImports.length) {
    lines.push('');
    lines.push('// Mock internal dependencies as needed:');
    for (const imp of internalImports) {
      const mock = flavor === 'vitest' ? 'vi.mock' : 'jest.mock';
      lines.push(`// ${mock}('${imp.modulePath}');`);
    }
  }
  lines.push('');

  // ── test suites ───────────────────────────────────────────────────────────
  for (const fn of analyzed.functions) {
    lines.push(...jvFunctionSuite(fn, flavor, options));
  }

  for (const cls of analyzed.classes) {
    lines.push(...jvClassSuite(cls, flavor, options));
  }

  return lines.join('\n');
}

function jvFunctionSuite(
  fn: FunctionInfo,
  flavor: 'jest' | 'vitest',
  options: GenerateOptions,
): string[] {
  const lines: string[] = [];
  const clearMocks =
    flavor === 'vitest' ? 'vi.clearAllMocks()' : 'jest.clearAllMocks()';

  lines.push(`describe('${fn.name}()', () => {`);
  lines.push(`  beforeEach(() => {`);
  lines.push(`    ${clearMocks};`);
  lines.push(`  });`);
  lines.push('');

  // happy path
  const callExpr = buildCallExpr(fn.name, fn.parameters);
  const awaitPrefix = fn.isAsync ? 'await ' : '';

  lines.push(`  it('should return the expected result for valid input', ${fn.isAsync ? 'async ' : ''}() => {`);
  lines.push(`    // TODO: set up valid inputs`);
  lines.push(`    ${buildArrange(fn.parameters)}`);
  lines.push(`    const result = ${awaitPrefix}${callExpr};`);
  lines.push(`    expect(result).toBeDefined();`);
  lines.push(`  });`);
  lines.push('');

  // error / invalid input
  if (fn.parameters.length > 0) {
    lines.push(`  it('should throw (or reject) when given invalid input', ${fn.isAsync ? 'async ' : ''}() => {`);
    lines.push(`    // TODO: substitute an invalid argument below`);
    if (fn.isAsync) {
      lines.push(`    await expect(${fn.name}(${nullArgs(fn.parameters)})).rejects.toThrow();`);
    } else {
      lines.push(`    expect(() => ${fn.name}(${nullArgs(fn.parameters)})).toThrow();`);
    }
    lines.push(`  });`);
    lines.push('');
  }

  // edge cases when coverage hints requested
  if (options.coverageHints) {
    lines.push(...jvEdgeCases(fn));
  }

  lines.push('});');
  lines.push('');
  return lines;
}

function jvClassSuite(
  cls: ClassInfo,
  flavor: 'jest' | 'vitest',
  options: GenerateOptions,
): string[] {
  const lines: string[] = [];
  const instanceVar = lcFirst(cls.name);
  const clearMocks =
    flavor === 'vitest' ? 'vi.clearAllMocks()' : 'jest.clearAllMocks()';

  lines.push(`describe('${cls.name}', () => {`);
  lines.push(`  let ${instanceVar}: ${cls.name};`);
  lines.push('');
  lines.push(`  beforeEach(() => {`);
  lines.push(`    ${clearMocks};`);
  if (cls.hasConstructor && cls.constructorParams.length > 0) {
    const ctorArgs = cls.constructorParams
      .map((p) => defaultValue(p))
      .join(', ');
    lines.push(`    // TODO: provide real constructor arguments`);
    lines.push(`    ${instanceVar} = new ${cls.name}(${ctorArgs});`);
  } else {
    lines.push(`    ${instanceVar} = new ${cls.name}();`);
  }
  lines.push(`  });`);
  lines.push('');

  for (const method of cls.methods) {
    lines.push(...jvMethodSuite(method, instanceVar, options));
  }

  lines.push('});');
  lines.push('');
  return lines;
}

function jvMethodSuite(
  method: MethodInfo,
  instanceVar: string,
  options: GenerateOptions,
): string[] {
  const lines: string[] = [];
  const callExpr = method.isStatic
    ? `${instanceVar.charAt(0).toUpperCase() + instanceVar.slice(1)}.${method.name}(${argsPlaceholder(method.parameters)})`
    : `${instanceVar}.${method.name}(${argsPlaceholder(method.parameters)})`;
  const awaitPrefix = method.isAsync ? 'await ' : '';

  lines.push(`  describe('${method.name}()', () => {`);
  lines.push(`    it('should return the expected result for valid input', ${method.isAsync ? 'async ' : ''}() => {`);
  lines.push(`      ${buildArrange(method.parameters, '      ')}`);
  lines.push(`      const result = ${awaitPrefix}${callExpr};`);
  lines.push(`      expect(result).toBeDefined();`);
  lines.push(`    });`);

  if (method.parameters.length > 0) {
    lines.push('');
    lines.push(`    it('should handle edge-case / invalid input gracefully', ${method.isAsync ? 'async ' : ''}() => {`);
    lines.push(`      // TODO: substitute invalid arguments to verify error handling`);
    if (method.isAsync) {
      lines.push(`      await expect(${awaitPrefix.trim()}${callExpr}).rejects.toThrow();`);
    } else {
      lines.push(`      expect(() => ${callExpr}).toThrow();`);
    }
    lines.push(`    });`);
  }

  if (options.coverageHints) {
    lines.push('');
    lines.push(`    it('should handle null / undefined inputs', () => {`);
    lines.push(`      // TODO: verify null-safety`);
    lines.push(`      expect(true).toBe(true); // replace with real assertion`);
    lines.push(`    });`);
  }

  lines.push(`  });`);
  lines.push('');
  return lines;
}

function jvEdgeCases(fn: FunctionInfo): string[] {
  const lines: string[] = [];
  const awaitPrefix = fn.isAsync ? 'await ' : '';
  const callExpr = buildCallExpr(fn.name, fn.parameters);

  lines.push(`  it('should handle empty / null inputs without crashing', ${fn.isAsync ? 'async ' : ''}() => {`);
  lines.push(`    // TODO: replace placeholder values`);
  lines.push(`    const result = ${awaitPrefix}${buildCallExpr(fn.name, fn.parameters, true)};`);
  lines.push(`    expect(result).toBeDefined();`);
  lines.push(`  });`);
  lines.push('');

  lines.push(`  it('should handle boundary values correctly', ${fn.isAsync ? 'async ' : ''}() => {`);
  lines.push(`    // TODO: test with min/max/zero values`);
  lines.push(`    const result = ${awaitPrefix}${callExpr};`);
  lines.push(`    expect(result).not.toBeNull();`);
  lines.push(`  });`);
  lines.push('');

  return lines;
}

// ─────────────────────────────────────────────────────────────────────────────
// Mocha generator
// ─────────────────────────────────────────────────────────────────────────────

function generateMocha(analyzed: AnalyzedFile, options: GenerateOptions): string {
  const lines: string[] = [];
  const sourceBase = moduleImportPath(analyzed.filePath);

  lines.push(`// Generated by create-test — mocha + chai`);
  lines.push(`import { expect } from 'chai';`);
  lines.push(`import sinon from 'sinon';`);
  lines.push('');

  const importedNames = buildImportList(analyzed);
  if (importedNames.length) {
    lines.push(`import { ${importedNames.join(', ')} } from '${sourceBase}';`);
  }
  lines.push('');

  const externalImports = analyzed.imports.filter((i) => i.isExternal);
  if (externalImports.length) {
    lines.push('// TODO: set up sinon stubs for external dependencies:');
    for (const imp of externalImports) {
      lines.push(`// import * as ${toCamelCase(imp.modulePath)} from '${imp.modulePath}';`);
    }
    lines.push('');
  }

  for (const fn of analyzed.functions) {
    lines.push(`describe('${fn.name}()', () => {`);

    lines.push(`  afterEach(() => sinon.restore());`);
    lines.push('');

    // happy path
    const awaitPrefix = fn.isAsync ? 'await ' : '';
    const callExpr = buildCallExpr(fn.name, fn.parameters);

    lines.push(`  it('should return expected result for valid input', ${fn.isAsync ? 'async ' : ''}() => {`);
    lines.push(`    ${buildArrange(fn.parameters)}`);
    lines.push(`    const result = ${awaitPrefix}${callExpr};`);
    lines.push(`    expect(result).to.exist;`);
    lines.push(`  });`);

    if (fn.parameters.length > 0) {
      lines.push('');
      lines.push(`  it('should throw for invalid input', ${fn.isAsync ? 'async ' : ''}() => {`);
      if (fn.isAsync) {
        lines.push(`    await expect(${fn.name}(${nullArgs(fn.parameters)})).to.be.rejected;`);
      } else {
        lines.push(`    expect(() => ${fn.name}(${nullArgs(fn.parameters)})).to.throw();`);
      }
      lines.push(`  });`);
    }

    if (options.coverageHints) {
      lines.push('');
      lines.push(`  it('should handle boundary values', () => {`);
      lines.push(`    // TODO: add boundary value assertions`);
      lines.push(`    expect(true).to.equal(true);`);
      lines.push(`  });`);
    }

    lines.push(`});`);
    lines.push('');
  }

  for (const cls of analyzed.classes) {
    const instanceVar = lcFirst(cls.name);

    lines.push(`describe('${cls.name}', () => {`);
    lines.push(`  let ${instanceVar}: ${cls.name};`);
    lines.push('');
    lines.push(`  beforeEach(() => {`);
    if (cls.hasConstructor && cls.constructorParams.length) {
      const ctorArgs = cls.constructorParams.map((p) => defaultValue(p)).join(', ');
      lines.push(`    // TODO: provide real constructor arguments`);
      lines.push(`    ${instanceVar} = new ${cls.name}(${ctorArgs});`);
    } else {
      lines.push(`    ${instanceVar} = new ${cls.name}();`);
    }
    lines.push(`  });`);
    lines.push(`  afterEach(() => sinon.restore());`);
    lines.push('');

    for (const method of cls.methods) {
      const call = `${instanceVar}.${method.name}(${argsPlaceholder(method.parameters)})`;
      const awaitPrefix = method.isAsync ? 'await ' : '';
      lines.push(`  describe('${method.name}()', () => {`);
      lines.push(`    it('should return expected result', ${method.isAsync ? 'async ' : ''}() => {`);
      lines.push(`      ${buildArrange(method.parameters, '      ')}`);
      lines.push(`      const result = ${awaitPrefix}${call};`);
      lines.push(`      expect(result).to.exist;`);
      lines.push(`    });`);
      lines.push(`  });`);
      lines.push('');
    }

    lines.push(`});`);
    lines.push('');
  }

  return lines.join('\n');
}

// ─────────────────────────────────────────────────────────────────────────────
// pytest generator
// ─────────────────────────────────────────────────────────────────────────────

function generatePytest(analyzed: AnalyzedFile, options: GenerateOptions): string {
  const lines: string[] = [];
  const moduleName = path
    .basename(analyzed.filePath)
    .replace(/\.py$/, '');

  lines.push(`# Generated by create-test — pytest`);
  lines.push(`import pytest`);
  lines.push('');

  // imports
  const allNames = [
    ...analyzed.functions.map((f) => f.name),
    ...analyzed.classes.map((c) => c.name),
  ];
  if (allNames.length) {
    lines.push(`from ${moduleName} import ${allNames.join(', ')}`);
  }

  // mock external imports
  const externalImports = analyzed.imports.filter((i) => i.isExternal);
  if (externalImports.length) {
    lines.push('');
    lines.push('# TODO: mock external dependencies with pytest-mock or unittest.mock:');
    for (const imp of externalImports) {
      lines.push(`# from unittest.mock import patch, MagicMock`);
      lines.push(`# @patch('${moduleName}.${imp.modulePath.split('/').pop()}')`);
    }
  }
  lines.push('');

  for (const fn of analyzed.functions) {
    const happyArgs = fn.parameters.map((p) => pyDefaultValue(p)).join(', ');

    // happy path
    lines.push('');
    lines.push(`class Test${capitalize(snakeToPascal(fn.name))}:`);
    lines.push('');
    lines.push(`    def test_${fn.name}_happy_path(self):`);
    lines.push(`        # TODO: provide valid arguments`);
    if (happyArgs) lines.push(`        # ${buildPyArrange(fn.parameters)}`);
    const callArgs = fn.parameters.map((p) => p.name).join(', ');
    lines.push(`        result = ${fn.name}(${callArgs || ''})`);
    lines.push(`        assert result is not None`);

    // error / edge case
    if (fn.parameters.length > 0) {
      lines.push('');
      lines.push(`    def test_${fn.name}_invalid_input_raises(self):`);
      lines.push(`        with pytest.raises(Exception):`);
      lines.push(
        `            ${fn.name}(${fn.parameters.map(() => 'None').join(', ')})`,
      );
    }

    if (options.coverageHints) {
      lines.push('');
      lines.push(`    def test_${fn.name}_boundary_values(self):`);
      lines.push(`        # TODO: test with empty strings, zero, negative numbers, large lists`);
      lines.push(`        assert True  # replace with real assertion`);
    }

    lines.push('');
  }

  for (const cls of analyzed.classes) {
    const fixtureVar = `${cls.name.toLowerCase()}_instance`;
    lines.push(`class Test${cls.name}:`);
    lines.push('');
    lines.push(`    @pytest.fixture`);
    lines.push(`    def ${fixtureVar}(self):`);
    if (cls.hasConstructor && cls.constructorParams.length) {
      const ctorArgs = cls.constructorParams.map((p) => pyDefaultValue(p)).join(', ');
      lines.push(`        # TODO: provide real constructor arguments`);
      lines.push(`        return ${cls.name}(${ctorArgs})`);
    } else {
      lines.push(`        return ${cls.name}()`);
    }
    lines.push('');

    for (const method of cls.methods) {
      const callArgs = method.parameters.map((p) => p.name).join(', ');
      lines.push(`    def test_${method.name}_happy_path(self, ${fixtureVar}):`);
      if (method.parameters.length) {
        lines.push(`        # TODO: set up arguments`);
        lines.push(`        ${buildPyArrange(method.parameters)}`);
      }
      lines.push(`        result = ${fixtureVar}.${method.name}(${callArgs})`);
      lines.push(`        assert result is not None`);
      lines.push('');
    }
  }

  return lines.join('\n');
}

// ─────────────────────────────────────────────────────────────────────────────
// Go test generator
// ─────────────────────────────────────────────────────────────────────────────

function generateGoTest(analyzed: AnalyzedFile, options: GenerateOptions): string {
  const lines: string[] = [];
  const fileDir = path.dirname(analyzed.filePath);
  const packageName = path.basename(fileDir) + '_test';

  lines.push(`// Generated by create-test — go test`);
  lines.push(`package ${packageName}`);
  lines.push('');
  lines.push(`import (`);
  lines.push(`\t"testing"`);

  const externalImports = analyzed.imports.filter((i) => i.isExternal);
  for (const imp of externalImports) {
    lines.push(`\t"${imp.modulePath}"`);
  }
  lines.push(`)`);
  lines.push('');

  for (const fn of analyzed.functions) {
    const testName = `Test${fn.name}`;

    lines.push(`func ${testName}_HappyPath(t *testing.T) {`);
    if (fn.parameters.length) {
      lines.push(`\t// TODO: provide valid arguments`);
      for (const p of fn.parameters) {
        lines.push(`\t${p.name} := ${goZeroValue(p.type)} // set a real value`);
      }
    }
    const callArgs = fn.parameters.map((p) => p.name).join(', ');
    lines.push(`\tresult${fn.returnType !== 'void' ? ', err' : ''} := ${fn.name}(${callArgs})`);
    if (fn.returnType !== 'void') {
      lines.push(`\tif err != nil {`);
      lines.push(`\t\tt.Fatalf("unexpected error: %v", err)`);
      lines.push(`\t}`);
      lines.push(`\tif result == nil {`);
      lines.push(`\t\tt.Fatal("expected non-nil result")`);
      lines.push(`\t}`);
    }
    lines.push(`}`);
    lines.push('');

    lines.push(`func ${testName}_ErrorCase(t *testing.T) {`);
    lines.push(`\t// TODO: pass invalid / zero arguments`);
    const zeroArgs = fn.parameters
      .map((p) => goZeroValue(p.type))
      .join(', ');
    lines.push(`\t_, err := ${fn.name}(${zeroArgs})`);
    lines.push(`\tif err == nil {`);
    lines.push(`\t\tt.Fatal("expected an error but got nil")`);
    lines.push(`\t}`);
    lines.push(`}`);
    lines.push('');

    if (options.coverageHints) {
      lines.push(`func ${testName}_TableDriven(t *testing.T) {`);
      lines.push(`\ttests := []struct {`);
      lines.push(`\t\tname    string`);
      for (const p of fn.parameters) {
        lines.push(`\t\t${p.name} ${p.type}`);
      }
      lines.push(`\t\twantErr bool`);
      lines.push(`\t}{`);
      lines.push(`\t\t{name: "happy path", wantErr: false},`);
      lines.push(`\t\t{name: "zero values", wantErr: true},`);
      lines.push(`\t}`);
      lines.push(`\tfor _, tt := range tests {`);
      lines.push(`\t\tt.Run(tt.name, func(t *testing.T) {`);
      const tableArgs = fn.parameters.map((p) => `tt.${p.name}`).join(', ');
      lines.push(`\t\t\t_, err := ${fn.name}(${tableArgs})`);
      lines.push(`\t\t\tif (err != nil) != tt.wantErr {`);
      lines.push(`\t\t\t\tt.Errorf("wantErr %v, got err %v", tt.wantErr, err)`);
      lines.push(`\t\t\t}`);
      lines.push(`\t\t})`);
      lines.push(`\t}`);
      lines.push(`}`);
      lines.push('');
    }
  }

  for (const cls of analyzed.classes) {
    for (const method of cls.methods) {
      const testName = `Test${cls.name}_${method.name}`;
      const callArgs = method.parameters.map((p) => p.name).join(', ');

      lines.push(`func ${testName}_HappyPath(t *testing.T) {`);
      lines.push(`\t// TODO: construct a real ${cls.name} instance`);
      lines.push(`\tvar sut ${cls.name}`);
      if (method.parameters.length) {
        lines.push(`\t// TODO: provide valid arguments`);
        for (const p of method.parameters) {
          lines.push(`\t${p.name} := ${goZeroValue(p.type)}`);
        }
      }
      lines.push(`\tresult${method.returnType !== 'void' ? ', err' : ''} := sut.${method.name}(${callArgs})`);
      if (method.returnType !== 'void') {
        lines.push(`\tif err != nil {`);
        lines.push(`\t\tt.Fatalf("unexpected error: %v", err)`);
        lines.push(`\t}`);
        lines.push(`\t_ = result`);
      }
      lines.push(`}`);
      lines.push('');
    }
  }

  return lines.join('\n');
}

// ─────────────────────────────────────────────────────────────────────────────
// Utilities shared by all generators
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Compute the relative import path to use in the generated test file.
 * Strips the extension and uses './' prefix.
 */
function moduleImportPath(sourceFilePath: string): string {
  const base = path.basename(sourceFilePath).replace(/\.[^.]+$/, '');
  return `./${base}`;
}

/** Collect the names to import from the module under test. */
function buildImportList(analyzed: AnalyzedFile): string[] {
  const names: string[] = [];

  for (const fn of analyzed.functions) {
    if (!fn.isDefault) names.push(fn.name);
  }
  for (const cls of analyzed.classes) {
    if (!cls.isDefault) names.push(cls.name);
  }

  return names;
}

/** Build an // arrange comment + variable declarations for params. */
function buildArrange(params: ParameterInfo[], indent = '    '): string {
  if (!params.length) return '// no parameters required';
  const lines = ['// TODO: replace with meaningful values'];
  for (const p of params) {
    lines.push(`const ${p.name} = ${defaultValue(p)};`);
  }
  return lines.join(`\n${indent}`);
}

function buildPyArrange(params: ParameterInfo[]): string {
  return params.map((p) => `${p.name} = ${pyDefaultValue(p)}`).join('; ');
}

/** Build a call expression for the named function with the given params. */
function buildCallExpr(
  name: string,
  params: ParameterInfo[],
  useNulls = false,
): string {
  if (!params.length) return `${name}()`;
  const args = useNulls
    ? params.map(() => 'null').join(', ')
    : params.map((p) => p.name).join(', ');
  return `${name}(${args})`;
}

/** Build a call argument list where every argument is null / undefined. */
function nullArgs(params: ParameterInfo[]): string {
  return params.map(() => 'null').join(', ');
}

/** Build a placeholder argument list using param names (for readability). */
function argsPlaceholder(params: ParameterInfo[]): string {
  return params.map((p) => p.name).join(', ');
}

/** Return a sensible TypeScript default value for a parameter. */
function defaultValue(p: ParameterInfo): string {
  if (p.defaultValue) return p.defaultValue;
  const t = p.type.toLowerCase();
  if (t.includes('string')) return `'TODO'`;
  if (t.includes('number') || t.includes('int') || t.includes('float')) return '0';
  if (t.includes('boolean') || t.includes('bool')) return 'false';
  if (t.includes('[]') || t.includes('array')) return '[]';
  return 'undefined as any'; // typed placeholder that forces a TODO
}

/** Return a sensible Python default value for a parameter. */
function pyDefaultValue(p: ParameterInfo): string {
  if (p.defaultValue) return p.defaultValue;
  const t = p.type.toLowerCase();
  if (t.includes('str')) return `'TODO'`;
  if (t.includes('int') || t.includes('float')) return '0';
  if (t.includes('bool')) return 'False';
  if (t.includes('list') || t.includes('[')) return '[]';
  if (t.includes('dict') || t.includes('{')) return '{}';
  return 'None';
}

/** Return a Go zero value for a type string. */
function goZeroValue(goType: string): string {
  const t = goType.trim();
  if (t === 'string') return '""';
  if (['int', 'int32', 'int64', 'uint', 'float32', 'float64'].includes(t)) return '0';
  if (t === 'bool') return 'false';
  if (t.startsWith('[]') || t.startsWith('map[')) return 'nil';
  if (t.startsWith('*')) return 'nil';
  return `${t}{}`; // struct zero value
}

// ── String helpers ──────────────────────────────────────────────────────────

function lcFirst(s: string): string {
  return s.charAt(0).toLowerCase() + s.slice(1);
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

function snakeToPascal(s: string): string {
  return s.split('_').map(capitalize).join('');
}

function toCamelCase(modulePath: string): string {
  const base = path.basename(modulePath).replace(/[^a-zA-Z0-9]/g, '_');
  return lcFirst(snakeToPascal(base));
}
