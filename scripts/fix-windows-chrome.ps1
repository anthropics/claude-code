# fix-windows-chrome.ps1
# Fixes Claude-in-Chrome on Windows when the WebSocket bridge (tengu_copper_bridge)
# is enabled server-side but fails to connect, breaking local named pipe fallback.
#
# What it does:
#   1. Ensures Claude Code is installed via npm (needed for Node.js runtime)
#   2. Patches cli.js to disable the WebSocket bridge, forcing local socket mode
#   3. Patches chrome-native-host.bat to route through Node.js + patched cli.js
#
# Usage:
#   powershell -ExecutionPolicy Bypass -File fix-windows-chrome.ps1
#
# After running, start Claude Code with:
#   %APPDATA%\npm\claude.cmd
#
# Re-run after every npm update -g @anthropic-ai/claude-code
#
# Related: https://github.com/anthropics/claude-code/issues/34788

param(
    [switch]$Uninstall
)

$ErrorActionPreference = "Stop"

$npmCli = "$env:APPDATA\npm\node_modules\@anthropic-ai\claude-code\cli.js"
$batFile = "$env:USERPROFILE\.claude\chrome\chrome-native-host.bat"
$nodeExe = (Get-Command node -ErrorAction SilentlyContinue).Source

if (-not $nodeExe) {
    Write-Host "ERROR: Node.js not found. Install from https://nodejs.org" -ForegroundColor Red
    exit 1
}

if (-not (Test-Path $npmCli)) {
    Write-Host "Installing Claude Code via npm..." -ForegroundColor Yellow
    npm install -g @anthropic-ai/claude-code
    if (-not (Test-Path $npmCli)) {
        Write-Host "ERROR: npm install failed." -ForegroundColor Red
        exit 1
    }
}

if ($Uninstall) {
    $code = Get-Content $npmCli -Raw -Encoding UTF8
    $patched  = 'function _Oz(){return;if(!w8("tengu_copper_bridge",!1))return;'
    $original = 'function _Oz(){if(!w8("tengu_copper_bridge",!1))return;'
    if ($code.Contains($patched)) {
        $code = $code.Replace($patched, $original)
        [System.IO.File]::WriteAllText($npmCli, $code, [System.Text.Encoding]::UTF8)
        Write-Host "Reverted cli.js patch." -ForegroundColor Green
    }
    Write-Host "Note: chrome-native-host.bat will be regenerated by Claude Code on next launch." -ForegroundColor Yellow
    exit 0
}

# Patch 1: cli.js (disable WebSocket bridge)
$code = Get-Content $npmCli -Raw -Encoding UTF8
$original = 'function _Oz(){if(!w8("tengu_copper_bridge",!1))return;'
$patched  = 'function _Oz(){return;if(!w8("tengu_copper_bridge",!1))return;'

if ($code.Contains($patched)) {
    Write-Host "[cli.js] Already patched." -ForegroundColor Green
} elseif ($code.Contains($original)) {
    $code = $code.Replace($original, $patched)
    [System.IO.File]::WriteAllText($npmCli, $code, [System.Text.Encoding]::UTF8)
    Write-Host "[cli.js] Patched - bridge disabled, local socket mode forced." -ForegroundColor Green
} else {
    Write-Host "[cli.js] WARNING: Pattern not found. Claude Code version may have changed." -ForegroundColor Red
}

# Patch 2: chrome-native-host.bat (route through Node.js)
$batDir = Split-Path $batFile -Parent
if (-not (Test-Path $batDir)) { New-Item -ItemType Directory -Path $batDir -Force | Out-Null }

@"
@echo off
REM Chrome native host wrapper script
REM Patched by fix-windows-chrome.ps1 to use Node.js + patched cli.js
"$nodeExe" "$npmCli" --chrome-native-host
"@ | Set-Content -Path $batFile -Encoding ASCII

Write-Host "[chrome-native-host.bat] Patched - using Node.js runtime." -ForegroundColor Green
Write-Host ""
Write-Host "Fix applied! Run Claude Code with: $env:APPDATA\npm\claude.cmd" -ForegroundColor Cyan
Write-Host "To revert: powershell -File $PSCommandPath -Uninstall" -ForegroundColor Gray
